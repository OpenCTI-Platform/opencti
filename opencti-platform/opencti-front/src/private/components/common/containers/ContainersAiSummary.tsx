import { graphql, PreloadedQuery, usePreloadedQuery } from 'react-relay';
import React from 'react';
import Grid from '@mui/material/Grid';
import Chip from '@mui/material/Chip';
import Alert from '@mui/material/Alert';
import { ContainersAiSummaryQuery } from './__generated__/ContainersAiSummaryQuery.graphql';
import useQueryLoading from '../../../../utils/hooks/useQueryLoading';
import { useFormatter } from '../../../../components/i18n';
import Loader, { LoaderVariant } from '../../../../components/Loader';
import { FilterGroup } from '../../../../utils/filters/filtersHelpers-types';
import MarkdownDisplay from '../../../../components/MarkdownDisplay';
import FilterIconButton from '../../../../components/FilterIconButton';

const containersAiSummaryQuery = graphql`
  query ContainersAiSummaryQuery(
    $first: Int
    $orderBy: ContainersOrdering
    $orderMode: OrderingMode
    $filters: FilterGroup
    $search: String
  ) {
    containersAiSummary(
      first: $first
      orderBy: $orderBy,
      orderMode: $orderMode
      filters: $filters
      search: $search
    ) {
      report
      topics
    }
  }
`;

interface ContainersAiSummaryComponentProps {
  stixCoreObjectId?: string;
  queryRef: PreloadedQuery<ContainersAiSummaryQuery>;
  filters: FilterGroup;
  first: number;
}

const ContainersAiSummaryComponent = ({ first, queryRef, filters }: ContainersAiSummaryComponentProps) => {
  const { t_i18n } = useFormatter();
  const { containersAiSummary } = usePreloadedQuery(
    containersAiSummaryQuery,
    queryRef,
  );
  if (containersAiSummary && containersAiSummary.report) {
    return (
      <>
        <Grid
          container={true}
          spacing={3}
          style={{ marginBottom: 20 }}
        >
          <Grid item={true} xs={9}>
            <FilterIconButton
              filters={filters}
              styleNumber={2}
              redirection={true}
            />
          </Grid>
          <Grid item={true} xs={3}>
            <Chip
              size="small"
              label={`${t_i18n('Last')} ${first} ${t_i18n('element(s)')}`}
              style={{ borderRadius: 4, height: 32, float: 'right', marginTop: 8 }}
            />
          </Grid>
        </Grid>
        <Alert severity="info" variant="outlined">
          {t_i18n('This summary is based on the whole content of related containers (description, content and attached files). It has been generated by XTM Copilot AI and can contain mistakes.')}
        </Alert>
        <MarkdownDisplay
          content={containersAiSummary.report}
          remarkGfmPlugin={true}
          commonmark={true}
          removeLinks={false}
        />
      </>
    );
  }

  return (
    <div
      style={{
        display: 'table',
        height: '100%',
        width: '100%',
        paddingTop: 15,
        paddingBottom: 15,
      }}
    >
      <span
        style={{
          display: 'table-cell',
          verticalAlign: 'middle',
          textAlign: 'center',
        }}
      >
        {t_i18n('No containers about this entity.')}
      </span>
    </div>
  );
};

interface ContainersAiSummaryProps {
  first: number
  filters: FilterGroup
}

const ContainersAiSummary = ({ first, filters }: ContainersAiSummaryProps) => {
  const queryRef = useQueryLoading<ContainersAiSummaryQuery>(
    containersAiSummaryQuery,
    {
      first,
      // eslint-disable-next-line @typescript-eslint/ban-ts-comment
      // @ts-ignore Excepts readonly array as variables but have simple array.
      filters,
      orderBy: 'created',
      orderMode: 'desc',
    },
  );
  return (
    <>
      {queryRef ? (
        <React.Suspense fallback={<Loader variant={LoaderVariant.inElement} />}>
          <ContainersAiSummaryComponent first={first} queryRef={queryRef} filters={filters}/>
        </React.Suspense>
      ) : (
        <Loader variant={LoaderVariant.inElement} />
      )}
    </>
  );
};

export default ContainersAiSummary;
